var radius = Number(prompt('Enter the radius of circle'));

function calcCircumference(radius) {
    console.log(`Circumference of circle = ${(radius * 2 * Math.PI)}`);
}

calcCircumference(radius)

function clacArea(radius) {
    console.log(`Area of circle  + ${(radius * radius * Math.PI)}`)
}

clacArea(radius)