var input = Number(prompt(`Please enter a number`));
document.write(`Number: ${input} <br />`);

var roundedVal = Math.round(input);
document.write(`Round off value: ${roundedVal} <br />`);

var floorVal = Math.floor(input)
document.write(`Floor value: ${floorVal} <br />`);

var ceilVal = Math.ceil(input)
document.write(`Ceil value: ${ceilVal} <br />`);